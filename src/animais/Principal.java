    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        if (nomeAnimal.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Por favor digite um nome para o animal.");
            return;
        }
        
        if(RBave.isSelected()==false && RBmamifero.isSelected()==false && RBreptil.isSelected()==false){
             JOptionPane.showMessageDialog(null, "Por favor selecione uma classificação.");
           return; 
        }

        Animal animal = new Animal();
        String reinoSelecionado = "";
        //Cria uma nova linha na tabela
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();

        if (RBave.isSelected()) {
            reinoSelecionado = "Ave";
            animal = new Ave();
        } else if (RBmamifero.isSelected()) {
            reinoSelecionado = "Mamifero";
            animal = new Mamifero();
        } else if (RBreptil.isSelected()) {
            reinoSelecionado = "Reptil";
            animal = new Reptil();
        }

        animal.setNome(nomeAnimal.getText());
        animal.setReinoAnimal(reinoSelecionado);
        System.out.println(campoID.getText());
        if (campoID.getText().equalsIgnoreCase("")) {
            animal.setId(listaDeAnimais.size() + 1);
        } else {
            animal.setId(Integer.parseInt(campoID.getText()));
            model.removeRow(numerodelinha);
            int index = listaDeAnimais.indexOf(animal);
            listaDeAnimais.remove(index);
        }

        listaDeAnimais.add(animal);

        //Adiciona dados vazios na tabela
        model.addRow(new Object[]{"", "", ""});

        //Adiciona o objeto na tabela
        tabela.getModel().setValueAt(animal.getId(), model.getRowCount() - 1, 0);
        tabela.getModel().setValueAt(animal.getNome(), model.getRowCount() - 1, 1);
        tabela.getModel().setValueAt(animal.getReinoAnimal(), model.getRowCount() - 1, 2);

        nomeAnimal.setText("");
        campoID.setText("");
        RBave.setSelected(false);
        RBmamifero.setSelected(false);
        RBreptil.setSelected(false);


    }//GEN-LAST:event_jButtonCadastrarActionPerformed

