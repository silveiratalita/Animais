    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        if (tabela.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Por favor selecione uma linha");
            return;
        }
        System.out.println(tabela.getSelectedRow());
        numerodelinha = tabela.getSelectedRow();
        String id = tabela.getModel().getValueAt(numerodelinha, 0).toString();
        String nomebixo = tabela.getModel().getValueAt(numerodelinha, 1).toString();
        String classificacaobixo = tabela.getModel().getValueAt(numerodelinha, 2).toString();

        if (classificacaobixo.equalsIgnoreCase("Ave")) {

            RBave.setSelected(true);
            RBreptil.setSelected(false);
            RBmamifero.setSelected(false);
        } else if (classificacaobixo.equalsIgnoreCase("Mamifero")) {

            RBmamifero.setSelected(true);
            RBave.setSelected(false);
            RBreptil.setSelected(false);
        } else if (classificacaobixo.equalsIgnoreCase("Reptil")) {

            RBreptil.setSelected(true);
            RBave.setSelected(false);
        }
        nomeAnimal.setText(nomebixo);
        campoID.setText(id);

        // }
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        if (tabela.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Por favor selecione uma linha");
            return;
          
        }
        
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        numerodelinha = tabela.getSelectedRow();
        Animal animal = new Animal();
        animal.setId((Integer) model.getValueAt(numerodelinha, 0));

        int numerolinhalista = listaDeAnimais.indexOf(animal);
        listaDeAnimais.remove(numerolinhalista);

        model.removeRow(numerodelinha);
    }//GEN-LAST:event_jButtonExcluirActionPerformed

